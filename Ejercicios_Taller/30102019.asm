TITLE TALLER
.MODEL SMALL
.STACK 64
.DATA

VAR1 db 'DIGITE NOMBRE: ','$'
VAR2 db 'DIGITE DIRECCION: ','$' 

dato1 LABEL BYTE
MAXLON1 DB 20
ACTLON1 DB ?           
CARDIG1 DB 20 DUP (' ')
REV1 DB 20 DUP (' ')


dato2 LABEL BYTE
MAXLON2 DB 20
ACTLON2 DB ?           
CARDIG2 DB 20 DUP (' ')
REV2 DB 20 DUP (' ')




.CODE
INICIO PROC FAC
    MOV AX, @data
    MOV DS,AX
    
    MOV ES,AX
    CALL LIMPIAR
    
    MOV DX,0000H
    CALL CURSOR
    LEA DX,VAR1
    CALL MOSTRAR
    LEA DX,dato1
    CALL DIGITAR
    
    
    MOV DX,0F0H
    CALL CURSOR
    LEA DX,VAR2
    CALL MOSTRAR
    LEA DX,dato2
    CALL DIGITAR
    
    CALL LIMPIAR
    
    MOV BH,00
    MOV BL,ACTLON1
    MOV CARDIG1[BX+1],'$'
    MOV DL,05
    MOV DH,05
    CALL CURSOR
    MOV AH,09H
    LEA DX,CARDIG1
    INT 21H
    
    
    MOV BH,00
    MOV BL,ACTLON2
    MOV CARDIG2[BX+1],'$'
    MOV DL,05H
    MOV DH,08H
    CALL CURSOR
    MOV AH,09H
    LEA DX,CARDIG2
    INT 21H
    
    CALL REV

    
    MOV AX,4C00H
    INT 21H
INICIO ENDP

MOSTRAR PROC NEAR
    MOV AH,09H
    INT 21H
    RET
MOSTRAR ENDP

CURSOR PROC NEAR
    MOV AH,02H
    MOV BH,00
    INT 10H
    RET
CURSOR ENDP

DIGITAR PROC NEAR
    MOV AH,0AH
    INT 21H
    RET
DIGITAR ENDP

FIN PROC NEAR
    MOV BH,00
    RET
FIN ENDP

INDICAR PROC NEAR
    MOV DL,05
    MOV DH,05
    CALL CURSOR
    MOV AH,09H
    INT 21H
    RET
INDICAR ENDP

LIMPIAR PROC NEAR
    MOV AH,06H
    MOV BH,30
    MOV CX,0000
    MOV DX,184FH
    INT 10H
    RET
LIMPIAR ENDP

REV PROC NEAR
    LEA DI,ACTLON1
    MOV BH,00
    MOV BL,[DI]
    LEA SI,REV1
    MOV CH,CARDIG1[BX] 
    MOV [SI],CH
REV ENDP