.CODE
INICIO PROC FAC
    MOV AX, @data
    MOV DS,AX
    
    MOV ES,AX
    CALL LIMPIAR
    
    MOV DX,0000H
    CALL CURSOR
    LEA DX,VAR1
    CALL MOSTRAR
    LEA DX,NUMERO1
    CALL DIGITAR
    
    
    MOV DX,0F0H
    CALL CURSOR
    LEA DX,VAR2
    CALL MOSTRAR
    LEA DX,NUMERO2
    CALL DIGITAR
    
    CALL LIMPIAR
    
    MOV DX,0F0H
    CALL CURSOR
    LEA DX,VAR3
    CALL MOSTRAR
    
    
    
    CALL SUMA
    
    
    MOV BH,00
    MOV DL,05
    MOV DH,05
    CALL CURSOR
    MOV AH,09H
    LEA DX,CARDIG3
