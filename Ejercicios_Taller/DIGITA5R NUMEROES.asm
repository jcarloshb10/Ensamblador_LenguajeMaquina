    ;MOV CARDIG3[1],AL
    
    ;call DIGITAR
    ;RET
    
    BANDERA: MOV CARDIG3[0],AL
    MOV CARDIG3[1],'$'
    RET
    

SUMA ENDP

MOSTRAR PROC NEAR
    MOV AH,09H
    INT 21H
    RET
MOSTRAR ENDP

CURSOR PROC NEAR
    MOV AH,02H
    MOV BH,00
    INT 10H
    RET
CURSOR ENDP

DIGITAR PROC NEAR
    MOV AH,0AH
    INT 21H
    RET
DIGITAR ENDP

FIN PROC NEAR
    MOV BH,00
    RET
FIN ENDP

INDICAR PROC NEAR
    MOV DL,05
    MOV DH,05
    CALL CURSOR
    MOV AH,09H
    INT 21H
    RET
INDICAR ENDP

LIMPIAR PROC NEAR
    MOV AH,06H
    MOV BH,30
    MOV CX,0000
    MOV DX,184FH
