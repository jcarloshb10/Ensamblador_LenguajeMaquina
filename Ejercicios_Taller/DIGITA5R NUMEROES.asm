    MOV AX,4C00H
    INT 21H
INICIO ENDP

SUMA PROC NEAR
    LEA SI,CARDIG1
    MOV AL,[SI]
    LEA DI, CARDIG2
    MOV BL,[DI]
    SUB AL,30H
    SUB BL,30H
    ADD AL,BL
    ADD AL,30H

    
    
    CMP AL,39H
    JBE  BANDERA
    
    MOV CARDIG3[0],31H
    SUB AL,0AH 

    
    
    MOV CARDIG3[1],AL
    MOV CARDIG3[2],'$'
    RET
   
    ;ADD AL,30H
